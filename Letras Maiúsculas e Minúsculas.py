# Solicita ao usuário que insira uma palavra
palavra = input("Digite uma palavra: ")

# Converte a palavra para minúsculas e maiúsculas
palavra_minuscula = palavra.lower()
palavra_maiuscula = palavra.upper()

# Exibe as palavras convertidas
print("Palavra em letras minúsculas:", palavra_minuscula)
print("Palavra em letras maiúsculas:", palavra_maiuscula)

#Encerrar o programa
input()
